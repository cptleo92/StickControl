<h3>What is Stick Control?</h3>

<p class="flex flex-col">
  <em class="mb-3">Taken directly from Amazon's description of the book: </em>

  George Lawrence Stone's Stick Control is the original classic, often called
  the bible of drumming. In 1993, Modern Drummer magazine named it first in the
  top 25 drumming books of all-time. In the words of the author, this is the
  ideal book for improving "control, speed, flexibility, touch, rhythm,
  lightness, delicacy, power, endurance, preciseness of execution, and muscular
  coordination," with extra attention given to the development of the weak hand.
  This indispensable book for drummers of all types includes hundreds of basic
  to advanced rhythms and moves through categories of single-beat combinations,
  triplets, short roll combinations, flam beats, flam triplets and dotted notes,
  and short roll progressions.
</p>

<h3>How do I use this tool?</h3>
<p>
  The book recommends playing each rhythm <strong>20 times</strong> without
  stopping, then moving on to the next rhythm. It's up to you how many patterns
  to practice. According to
  <a
    class="text-blue-600 hover:underline hover:text-blue-800"
    href="https://www.youtube.com/watch?v=Du31MCIlYO0&ab_channel=Drumeo"
    target="_blank"
    rel="noreferrer">Drumeo's Stick Control Challenge</a
  >, the recommendation is to play through <strong>24 patterns</strong> at a
  time, which should take a little less than 30 minutes at
  <strong>152 BPM</strong>.
</p>
<p>
  Alternatively, spend <strong>60 seconds</strong> on each pattern before moving
  on. The tool allows you to choose whichever practice method you prefer.
</p>
<p>
  The key is to play each stroke as <em>precisely as possible</em> while also staying
  relaxed and free of tension. If you're struggling with keeping up, lower the BPM
  until you can play each pattern consistently. Practice this every day and you'll
  see massive improvements in your drumming.
</p>
<p>
  And of course, please support the original source by purchasing a copy of
  Stick Control!
</p>

